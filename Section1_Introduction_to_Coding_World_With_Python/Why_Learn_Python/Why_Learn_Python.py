# ---------------------------------------------------
# Why Learn Python? (Simple Notes)
# ---------------------------------------------------

# 1. Easy to Learn
#    - Beginner-friendly, simple syntax
#    - You can write useful programs quickly

# 2. Portable
#    - Same Python code runs on Windows, Mac, Linux
#    - No need for same system everywhere

# 3. Readable
#    - Code is clean, human-friendly
#    - Easy to guess what code is doing

# 4. Productive
#    - Less code compared to Java / C
#    - Faster to build projects

# 5. Huge Standard Library (STL)
#    - Pre-written code available for free
#    - Open-source libraries for almost everything
#    - Example: data science, ML, AI, automation

# 6. Multi-use Language
#    - Web apps (full-stack possible)
#    - Automation scripts
#    - Data science / Machine Learning / AI
#    - CSV handling / data processing
#    - General-purpose programming

# 7. Fun to Use
#    - Coding in Python feels simple & enjoyable
#    - "Chai-level happiness" :)

# ---------------------------------------------------
# Summary:
# Python = Easy + Portable + Productive + Multi-use
# ---------------------------------------------------
